WITH  assign_row_number AS (
    SELECT *, ROW_NUMBER() over (PARTITION BY ID ORDER BY ID ASC) AS row, (ROW_NUMBER() over (PARTITION BY ID ORDER BY ID ASC) + 1) AS row2 FROM records
),
matching_row AS (
    SELECT a1.ID AS r, a1.row AS f1, a2.row as f2, a1.Val AS v1, a2.Val AS v2, (a1.Val - a2.Val) as d, CASE WHEN (a1.Val - a2.Val) > 0 THEN 1 WHEN (a1.Val - a2.Val) < 0 THEN -1 ELSE 0 END AS direction FROM assign_row_number a1
    JOIN assign_row_number a2 ON a1.ID = a2.ID AND a1.row = a2.row2
),
increasing_row AS (
    SELECT r, f1, f2, v1, v2, d, direction FROM matching_row WHERE direction > 0
),
decreasing_row AS (
    SELECT r, f1, f2, v1, v2, d, direction FROM matching_row WHERE direction < 0
),
valid_step_row AS (
    SELECT r, f1, f2, v1, v2, d, direction FROM matching_row WHERE ABS(d) > 0 AND ABS(d) < 4
),
counting_diff_row AS (
    SELECT r, COUNT(*) AS c FROM matching_row GROUP BY r ORDER BY r ASC
),
counting_increasing_row AS (
    SELECT r, COUNT(*) AS c FROM increasing_row GROUP BY r ORDER BY r ASC
),
counting_decreasing_row AS (
    SELECT r, COUNT(*) AS c FROM decreasing_row GROUP BY r ORDER BY r ASC
),
counting_valid_step_row AS (
    SELECT r, COUNT(*) AS c FROM valid_step_row GROUP BY r ORDER BY r ASC
),
valid_inc AS (
    SELECT count_all.r, count_all.c FROM counting_diff_row as count_all JOIN counting_increasing_row AS count_inc ON count_all.r = count_inc.r WHERE count_all.c = count_inc.c
),
valid_dec AS (
    SELECT count_all.r, count_all.c FROM counting_diff_row as count_all JOIN counting_decreasing_row AS count_dec ON count_all.r = count_dec.r WHERE count_all.c = count_dec.c
),
valid_diff AS (
    SELECT count_all.r, count_all.c FROM counting_diff_row as count_all JOIN counting_valid_step_row AS count_diff ON count_all.r = count_diff.r WHERE count_all.c = count_diff.c
),
merge_all AS (
    SELECT DISTINCT counting_diff_row.r FROM counting_diff_row
    JOIN valid_inc ON counting_diff_row.r = valid_inc.r
    JOIN valid_diff ON counting_diff_row.r = valid_diff.r
    WHERE counting_diff_row.c = valid_inc.c  AND counting_diff_row.c = valid_diff.c
    UNION
    SELECT DISTINCT counting_diff_row.r FROM counting_diff_row
    JOIN valid_dec ON counting_diff_row.r = valid_dec.r
    JOIN valid_diff ON counting_diff_row.r = valid_diff.r
    WHERE counting_diff_row.c = valid_dec.c  AND counting_diff_row.c = valid_diff.c
)
SELECT COUNT(*) FROM merge_all;
