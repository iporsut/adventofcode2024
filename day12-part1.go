type plot struct {
	plantType rune
	x, y      int
	perimeter int
}

var marker = map[plot]bool{}

func calcPerimeter(garden [][]rune, mx, my int, p plot) int {
	var perimeter int
	if p.x-1 < 0 {
		perimeter++
	} else if garden[p.y][p.x-1] != p.plantType {
		perimeter++
	}

	if p.y-1 < 0 {
		perimeter++
	} else if garden[p.y-1][p.x] != p.plantType {
		perimeter++
	}

	if p.x+1 >= mx {
		perimeter++
	} else if garden[p.y][p.x+1] != p.plantType {
		perimeter++
	}

	if p.y+1 >= my {
		perimeter++
	} else if garden[p.y+1][p.x] != p.plantType {
		perimeter++
	}

	return perimeter
}

func findPlotInSameRegion(garden [][]rune, mx, my int, plantType rune, x, y int) []plot {
	var plots []plot
	pt := plot{plantType: plantType, x: x, y: y}
	marker[pt] = true
	// calculate perimeter
	pt.perimeter = calcPerimeter(garden, mx, my, pt)

	plots = append(plots, pt)

	if y-1 >= 0 && garden[y-1][x] == plantType && !marker[plot{plantType: plantType, x: x, y: y - 1}] {
		plots = append(plots, findPlotInSameRegion(garden, mx, my, plantType, x, y-1)...)
	}

	if x+1 < mx && garden[y][x+1] == plantType && !marker[plot{plantType: plantType, x: x + 1, y: y}] {
		plots = append(plots, findPlotInSameRegion(garden, mx, my, plantType, x+1, y)...)
	}

	if y+1 < my && garden[y+1][x] == plantType && !marker[plot{plantType: plantType, x: x, y: y + 1}] {
		plots = append(plots, findPlotInSameRegion(garden, mx, my, plantType, x, y+1)...)
	}

	if x-1 >= 0 && garden[y][x-1] == plantType && !marker[plot{plantType: plantType, x: x - 1, y: y}] {
		plots = append(plots, findPlotInSameRegion(garden, mx, my, plantType, x-1, y)...)
	}

	return plots
}
